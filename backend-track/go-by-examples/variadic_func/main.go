package main

import (
	"fmt"
)

func sum(nums ...int) int {
	fmt.Print(nums, " ")
	total := 0
	for i, num := range nums {
		total += num + nums[i]
	}
	return total
}
func main() {

	ans := sum(1, 2, 3, 4, 5)
	fmt.Println("ans", ans)
}

