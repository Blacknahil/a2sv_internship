package main

import "fmt"

func zeroval(ival int) {
	ival = 0
}

func zeroptr(iptr *int) {
	fmt.Println("inside zeroptr address", iptr)
	fmt.Println("inside zeroptr value=", *iptr)

	*iptr = 0
}
func main() {

	i := 19
	fmt.Println("intial=", i)

	zeroval(i)
	fmt.Println("zeroval", i)

	zeroptr(&i)
	fmt.Println("zeroptr", i)

	fmt.Println("pointer/address", &i)

}

