package main

import (
	"fmt"
)

func plus(a int, b int) int {
	return a + b
}

func plusplus(a, b, c int) int {
	return a + b + c
}

func main() {

	res := plus(10, 11)
	fmt.Println("10+11", res)

	res = plusplus(10, 11, 21)
	fmt.Println("res", res)
}

