package main

import (
	"errors"
	"fmt"
)

type argError struct {
	arg     int
	message string
}

func (e *argError) Error() string {
	return fmt.Sprintf("%d - %s", e.arg, e.message)
}

func f(arg int) (int, error) {

	if arg == 42 {
		return -1, &argError{arg, "can't with it"}
	}
	return arg + 109, nil
}

func main() {
	_, err := f(42)
	var ae *argError
	if errors.As(err, &ae) {
		fmt.Println("arg:", ae.arg)
		fmt.Println("message:", ae.message)
	} else {
		fmt.Println("err doesn't match argError")
	}
}

