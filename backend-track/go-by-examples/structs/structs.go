package main

import "fmt"

type person struct {
	name string
	age  int
}

func newPerson(name string) *person {

	p := person{name: name}
	p.age = 21
	return &p
}

func main() {

	fmt.Println(person{"nahom", 21})
	fmt.Println(person{name: "Fred"})

	fmt.Println(&person{name: "beth", age: 21})
	fmt.Println(&person{name: "Ann", age: 40})

	fmt.Println(newPerson("nahom"))

	s := person{"semi", 23}
	fmt.Println(s.name)

	sp := &s
	fmt.Println(sp.age)
	fmt.Println("sp=", sp)

	sp.age = 51
	fmt.Println(sp.age)

	dog := struct {
		name   string
		isGood bool
	}{
		"Bobi",
		false,
	}

	fmt.Println(dog)
	fmt.Printf("%p\n", &dog)

}

