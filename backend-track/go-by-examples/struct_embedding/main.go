package main

import "fmt"

// define a strcut called base
type base struct {
	num int
}

// add a method called describe
func (b base) describe() string {
	return fmt.Sprintf("base with num= %v", b.num)
}

// define a struct called container
type container struct {
	// embed the base strcut with it
	base
	str string
}

func main() {

	co := container{
		base: base{
			num: 1,
		},
		str: "docker",
	}

	fmt.Printf("co ={num: %v, str : %v} \n", co.num, co.str)

	fmt.Println("also num = ", co.base.num)

	fmt.Println("descibe co: ", co.describe())

	type describer interface {
		describe() string
	}

	var d describer = co
	fmt.Println("describer :", d.describe())
	fmt.Println(d)

}

